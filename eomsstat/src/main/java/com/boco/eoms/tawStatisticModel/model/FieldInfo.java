package com.boco.eoms.tawStatisticModel.model;

public class FieldInfo {
	
	private String fieldId;
	private String fieldCode;
	private String fieldCnName;
	private String fieldValue;
	private String fieldTurnVlaue;
	public String getFieldId() {
		return fieldId;
	}
	public void setFieldId(String fieldId) {
		this.fieldId = fieldId;
	}
	public String getFieldCode() {
		return fieldCode;
	}
	public void setFieldCode(String fieldCode) {
		this.fieldCode = fieldCode;
	}
	public String getFieldCnName() {
		return fieldCnName;
	}
	public void setFieldCnName(String fieldCnName) {
		this.fieldCnName = fieldCnName;
	}
	public String getFieldValue() {
		return fieldValue;
	}
	public void setFieldValue(String fieldValue) {
		this.fieldValue = fieldValue;
	}
	public String getFieldTurnVlaue() {
		return fieldTurnVlaue;
	}
	public void setFieldTurnVlaue(String fieldTurnVlaue) {
		this.fieldTurnVlaue = fieldTurnVlaue;
	}
	
	
	
	
}
